package logic;

import java.util.Date;

public class Goodorbad {

	
	private int no;
	private int wno;
	private int gno;
	private String name;
	private int point;
	private Date regdate;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getWno() {
		return wno;
	}
	public void setWno(int wno) {
		this.wno = wno;
	}
	public int getGno() {
		return gno;
	}
	public void setGno(int gno) {
		this.gno = gno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public Date getRegdate() {
		return regdate;
	}
	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}
	@Override
	public String toString() {
		return "goodorbad [no=" + no + ", wno=" + wno + ", gno=" + gno + ", name=" + name + ", point=" + point
				+ ", regdate=" + regdate + "]";
	}
	
	
}
